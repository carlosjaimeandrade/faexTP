# Variáveis booleanas armazenam apenas dois valores: True ou False

# Exemplo de variável booleana
chovendo = True  # significa que está chovendo

# Podemos usar essas variáveis em estruturas condicionais (if/else)
if chovendo:
    print("Está chovendo. Pegue um guarda-chuva!")
else:
    print("Não está chovendo. Você pode sair sem guarda-chuva.")

